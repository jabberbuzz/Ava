<h2>What is Library in Ava ?</h2>
<p>
    Library is core class of Ava. You can call library from $this. It not like plugin. Library will load in base class. plugin will load only in one class or one page. When you load library, you can call from everywhere of the framework
</p>

<p>Base library are</p>
<ul>
    <li>io</li>
    <li>segment</li>
    <li>router</li>
</ul>

